    private void btnDetalhesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetalhesActionPerformed
               
        int linhaSelecionada = tblRelatorioSintetico.getSelectedColumn();
        
        if(linhaSelecionada >= 0){
        DefaultTableModel modelo = (DefaultTableModel) tblRelatorioSintetico.getModel();
        int idVenda = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
        
        ArrayList<com.mycompany.sistemapadaria.models.RelatorioAnalitico> lista = RelatorioAnaliticoDAO.listarPorVenda(idVenda);
        
        DefaultTableModel modeloAnalitico = (DefaultTableModel)tblRelatorioDetalhado.getModel();
        modeloAnalitico.setRowCount(0);
        
        for (com.mycompany.sistemapadaria.models.RelatorioAnalitico item : lista){
            
            
            modeloAnalitico.addRow(new String[]{
                item.getNomeProduto(),
                String.valueOf(item.getQtdProduto()),
                String.valueOf(item.getVlrUnitario())
            });
            
        }
    }
        
    }//GEN-LAST:event_btnDetalhesActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
     
        Date dtInicio = jdcDtInicio.getDate();
        Date dtTermino = jdcDtTermino.getDate();
        
      ArrayList<RelatorioSintetico> lista = RelatorioSinteticoDAO.listarPorPeriodo(dtInicio, dtTermino);
       
       DefaultTableModel modelo = (DefaultTableModel) tblRelatorioSintetico.getModel();
       modelo.setRowCount(0);
       
       for(RelatorioSintetico item: lista){
           
           
           modelo.addRow(new String[]{
           String.valueOf(item.getIdVenda()),
           String.valueOf(item.getDataVenda()),
           item.getNomeCLiente(),
           String.valueOf(item.getValorVenda())
       });
           
       }
       
    }//GEN-LAST:event_btnPesquisarActionPerformed

